package chap2.variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수

        byte b = 127; // -128 ~ 127
        short s = 32767; // - 32768 ~ 32767
        int i = 2147483647; // 20억까지 쓸 수 있음

        long l = 9256465132618L; // 그냥 엄~~청 크게 가능

        //실수

        float f = 10.0f; //
        double d = 10.0;

        System.out.println();

    }
}
